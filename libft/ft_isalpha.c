#include "libft.h"

int	ft_isalpha(int x)
{
	if ('a' <= x && x <= 'z')
	{
		return (1);
	}
	else if ('A' <= x && x <= 'Z')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
