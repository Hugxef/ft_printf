#include "libft.h"

int	ft_isalnum(int x)
{
	if ('0' <= x && x <= '9')
	{
		return (1);
	}
	else if ('a' <= x && x <= 'z')
	{
		return (1);
	}
	else if ('A' <= x && x <= 'Z')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
